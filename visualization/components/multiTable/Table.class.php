<?php

/**
 * Table entity to hold data for visualization.
 *
 * Contains Header, Row header and Rows.
 *
 * @version 1.0
 * @author Filip
 */
class Table
{
    /**
     * Header of table
     * @var mixed
     */
    public $Header;

    /**
     * Rows of table
     * @var mixed
     */
    public $Rows;

    /**
     * Header row
     * @var mixed
     */
    public $HeaderRow;
}
